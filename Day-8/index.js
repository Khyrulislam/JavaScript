

var digit = prompt("Ente any Number : ");

// Digit seciton 

switch(digit){
        case "0" :
            document.write(digit + " = " +"Zero");
            break
        case "1" :
            document.write(digit + " = " +"One");
            break    
        case "2" :
            document.write(digit + " = " +"Two");
            break
        case "3" :
            document.write(digit + " = " +"Tree");
            break
        case "4" :
            document.write(digit + " = " +"Four");
            break
        case "5" :
            document.write(digit + " = " +"Five");
            break        
        case "6" :
            document.write(digit + " = " +"Six");
            break
        case "7" :
            document.write(digit + " = " +"Seven");
            break
        case "8" :
            document.write(digit + " = " +"Eight");
            break
        case "9" :
            document.write(digit + " = " +"Nine");
            break
        default :
            document.write(digit + " = " +"Not a right Digit")

}